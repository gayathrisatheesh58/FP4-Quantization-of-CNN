# Step 12.4.1: FP4 quantization with scaling
# We do this scaling to overcome the error porduced during the direct quantisation step
def quantize_tensor_fp4_scaled(tensor):

    tensor = tensor.detach().cpu()


    # Find maximum absolute value

    max_value = torch.max(
        torch.abs(tensor)
    )


    # Calculate scale

    scale = max_value / 1.75


    # Normalize

    normalized_tensor = tensor / scale


    # Quantize normalized values

    quantized_tensor = torch.zeros_like(
        normalized_tensor
    )


    for index, value in np.ndenumerate(
        normalized_tensor.numpy()
    ):

        quantized_tensor[index] = quantize_fp4(value)


    # Convert back to original range

    reconstructed_tensor = (
        quantized_tensor * scale
    )


    return reconstructed_tensor, scale

