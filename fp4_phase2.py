"""Currently we did FP4 + one scale down, It proved less useful os we try BLOCK WISE FP4 QUANTISATION

In blockwise quantisation we do the following steps ,
1. Split each others weights into blocks (32 or 64)
2. Calculate one scale per block
3. Quantise each block seperately
4. Reconstruct weights
5. Compare error


Here each block has its own scale so that
0.002 , 0.0034 like that and

0.3,0.34 has diferent scale

# IMPLEMENT BLOCWISE FP4 QUANTIZER
"""

# Step 12.5.1: Block-wise FP4 quantization

def quantize_tensor_fp4_blockwise(tensor, block_size=32):

    tensor_flat = tensor.detach().cpu().flatten()


    quantized_flat = torch.zeros_like(
        tensor_flat
    )


    scales = []


    # Process blocks

    for i in range(0, len(tensor_flat), block_size):

        block = tensor_flat[i:i+block_size]


        # Calculate scale for this block

        max_value = torch.max(
            torch.abs(block)
        )


        if max_value == 0:
            scale = 1.0

        else:
            scale = max_value / 1.75


        scales.append(scale)


        # Normalize block

        normalized_block = block / scale


        # Quantize values

        quantized_block = torch.zeros_like(
            normalized_block
        )


        for index, value in enumerate(normalized_block):

            quantized_block[index] = quantize_fp4(
                value.item()
            )


        # Restore scale

        reconstructed_block = (
            quantized_block * scale
        )


        quantized_flat[i:i+block_size] = reconstructed_block


    return (
        quantized_flat.reshape(tensor.shape),
        scales
    )

"""**Divide weights into small blocks, find a scale for each block, convert the values inside the block to FP4, then multiply back by the scale.**"""

