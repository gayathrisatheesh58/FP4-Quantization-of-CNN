# Step 12.1: Quantize a tensor using FP4

def quantize_tensor_fp4(tensor):

    tensor_np = tensor.detach().cpu().numpy()

    quantized_tensor = np.zeros_like(tensor_np)


    # Convert every value to nearest FP4 value

    for index, value in np.ndenumerate(tensor_np):

        quantized_tensor[index] = quantize_fp4(value)


    return torch.tensor(
        quantized_tensor,
        dtype=torch.float32
    )

