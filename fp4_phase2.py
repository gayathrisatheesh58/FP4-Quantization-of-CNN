import math


def create_fp4_table():

    values = []

    # Sign bit
    for sign in [0, 1]:

        # Exponent bits
        for exponent in range(4):

            # Mantissa bit
            for mantissa in [0, 1]:

                # bias for E2M1
                bias = 1

                if exponent == 0:
                    value = 0
                else:
                    value = (
                        (-1)**sign *
                        (1 + mantissa*0.5) *
                        2**(exponent-bias)
                    )

                values.append(value)


    return sorted(set(values))


